//自己連結
class Main {
  public static void main(String[] args) {
    int month = 2;
    int date = 1;
    
    // 「2月1日」
    System.out.println( month + "月" + date + "日");
    System.out.println(2/1);
    System.out.println(2.0/1.0);
    System.out.println(2.0/1);
  }
}